    SET @NEW_MA = 'KH' + RIGHT('0000' + CAST(@NEXT_NUM AS VARCHAR), 4);

    INSERT INTO KHACHHANG (MAKH, MALKH, TENKH, SDT, DIEMTL)
    VALUES (@NEW_MA, @MALKH, @TENKH, @SDT, @DIEMTL);
END;
GO

